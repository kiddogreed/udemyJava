

/**
 * Operators
 */
public class Operators {

  public static void main(String[] args) {

    int res, x, add, sub, mul, div;
    x =  10;
    add = x + 10;
    sub = x -5;
    mul = x * 2;
    div = x / 2;
    res = add;
    System.out.println(x+"+10 = "+res);
    res = sub;
    System.out.println(x+"-5 = "+res);
    res = mul;
    System.out.println(x+"*2 = "+res);
    res = div;
    System.out.println(x+"/2 = "+res);
    
  }
}